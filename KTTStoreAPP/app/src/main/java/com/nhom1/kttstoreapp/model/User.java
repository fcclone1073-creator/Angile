package com.nhom1.kttstoreapp.model;

public class User {
    private String _id;
    private String name;
    private String email;
    private String phone;
    private String gender;

    public String getId() {
        return _id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getPhone() {
        return phone;
    }

    public String getGender() {
        return gender;
    }
}
